class ApiErrorHandler extends Error {
  
  constructor(
    statusCode,
    message = "Somthing went Wrong : Not get the Error Message",
    errors = [],
    stack = ""
  ) {
    super(message);
    this.statusCode = statusCode;
    this.data = null;
    this.message = message;
    this.success = false;
    this.errors = errors;

    if (stack) {
      this.stack = stack;
    } else {
      Error.captureStackTrace(this, this.constructor);
    }
  }
}


export { ApiErrorHandler }